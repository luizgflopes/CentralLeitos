    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        em.getTransaction().begin();
        
        Usuario u;
        if(this.idUsuario == null){
            u = new Usuario();
            em.persist(u);
            
        } else {
            u = em.find(Usuario.class, idUsuario);
            em.merge(u);
        }
        
        u.setNome(txtNome.getText());
        u.setLogin(txtLogin.getText());
        u.setSenha(String.valueOf(txtSenha.getPassword()));
        u.setAtivo(cbxAtivo.isSelected());
        
        
        em.getTransaction().commit();
        JOptionPane
                .showMessageDialog(this,"Comando efetuado com sucesso!");
        btnLimpar.doClick();
        btnBuscar.doClick();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        List <Usuario> usuarios = em
                .createQuery("SELECT u FROM Usuario u")
                .getResultList();
        
        DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
        
        model.setRowCount(0);
        for(Usuario u : usuarios) {
            model.addRow(
                new Object[] { 
                    u.getId(),
                    u.getNome(),
                    u.getLogin(),
                    u.getAtivo() ? "Sim" : "Não"
                    
                }
            );
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int row = tblUsuarios.getSelectedRow();
        
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Favor selecionar um usuário.");
        } else {
            Integer id = (Integer) tblUsuarios.getModel().getValueAt(row, 0);
            
            
            Usuario u = em.find(Usuario.class, id);
            em.getTransaction().begin();
            em.remove(u);
            em.getTransaction().commit();
            
            btnBuscar.doClick();
            
            JOptionPane.showMessageDialog(this, "Usuário excluido com sucesso!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        // TODO add your handling code here:
        
        int row = tblUsuarios.getSelectedRow();
        
        if(row > -1 && evt.getClickCount()==2){
            Integer id = (Integer) tblUsuarios.getModel().getValueAt(row, 0);
            
            Usuario u = em.find(Usuario.class, id);
            this.idUsuario = u.getId();
            txtNome.setText(u.getNome());
            txtLogin.setText(u.getLogin());
            txtSenha.setText(u.getSenha());
            cbxAtivo.setSelected(u.getAtivo());
            
            
        }
    }//GEN-LAST:event_tblUsuariosMouseClicked

